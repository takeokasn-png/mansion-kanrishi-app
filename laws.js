function showLawRef(law, art){
  ensureLawModal();
  const db = LAW_DB[law];
  if(!db) return;
  const entry = db.arts ? db.arts[art] : null;
  document.getElementById('lawTitle').textContent = law + ' 第' + art + '条' + (entry && entry.t ? '（' + entry.t + '）' : '');
  document.getElementById('lawSub').textContent = db.note || '';
  const body = document.getElementById('lawBody');
  const note = document.getElementById('lawNote');
  if(entry){
    body.textContent = entry.s;
    note.textContent = '※学習用の要旨です。正確な条文はe-Gov法令検索でご確認ください。';
  } else {
    body.textContent = 'この条文の要旨は収録されていません。';
    note.textContent = db.egov ? '下のリンクからe-Gov法令検索で全文を確認できます。' : '';
  }
  const link = document.getElementById('lawLink');
  const url = lawEgovUrl(db, art);
  if(url){
    link.href = url;
    link.textContent = 'e-Govで条文全文を見る ↗';
    link.style.display = 'block';
  } else {
    link.style.display = 'none';
  }
  document.getElementById('lawOverlay').classList.add('show');
}

function closeLawRef(){
  const ov = document.getElementById('lawOverlay');
  if(ov) ov.classList.remove('show');
}
